window.onload = () => {
  // Al caricamento della pagina voglio mostrare tutte le card

  // 1. Seleziono il contenitore delle cards "songs-list"
  const cardContainer = document.getElementById("songs-list")

  // 2. Per ogni elemento dell'array data devo creare una card (una stringa contenente un div)

  const arrayDiCards = data.map(song => cardTemplate(song))

  const stringone = arrayDiCards.join("")

  // 3. Una volta create tutte le card devo inserirle nel DOM all'interno del contenitore
  cardContainer.innerHTML = stringone

  // ************************************ FILTRA CARDS *************

  // 1. Seleziono l'input field
  const input = document.getElementById("search")

  // 2. Aggiungere un evento all'input field
  input.addEventListener("change", e => {
    // 3. Questo evento eseguirà una funzione che filtra le canzoni
    console.log(e.target.value.toLowerCase())
    const chiaveDiRicerca = e.target.value.toLowerCase()

    const filteredSongs = data.filter(song => song.title.toLowerCase().includes(chiaveDiRicerca))
    console.log(filteredSongs)

    cardContainer.innerHTML = filteredSongs.map(song => cardTemplate(song)).join("")
  })
}
